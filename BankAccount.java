public class BankAccount{
  String Nome;
  double Saldo;
  int Idade;

  //Constructor
  public BankAccount(String Nome, double Saldo, int Idade) {
    this.Nome = Nome;
    this.Saldo = Saldo;
    this.Idade = Idade;
  }
  //Getters and Setters
  public String getNome() {
    return Nome;
  }

  

}
